#pragma once
#include <ntddk.h>

#define IO_GET_PROCESS_LIST CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1724, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)

#define IO_COPY_MEMORY CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1725, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)

#define IO_UNLOAD_DRIVER CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1726, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)

#define IO_WRITE_MEMORY CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1727, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)

#define IO_READ_KERNEL  CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1728, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)

#define IO_DSE_SET_ADDR CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1730, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)
#define IO_DSE_DISABLE  CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1731, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)
#define IO_DSE_ENABLE   CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1732, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)
#define IO_DSE_QUERY    CTL_CODE(FILE_DEVICE_UNKNOWN, 0x1733, METHOD_BUFFERED, FILE_SPECIAL_ACCESS)

#define DSE_MAX_ADDRS 8

typedef struct _KERNEL_DSE_OPERATION
{
	ULONG   count;
	ULONG64 addresses[DSE_MAX_ADDRS];
	ULONG   originals[DSE_MAX_ADDRS];
} KERNEL_DSE_OPERATION, *PKERNEL_DSE_OPERATION;

typedef struct _KERNEL_PROCESS_LIST_OPERATION
{
	PVOID bufferAddress;
	INT32 bufferSize;
	INT32 processCount;
} KERNEL_PROCESS_LIST_OPERATION, *PKERNEL_PROCESS_LIST_OPERATION;

typedef struct _KERNEL_READ_KERNEL_OPERATION
{
	ULONG64 kernelAddress;
	PVOID   bufferAddress;
	INT32   bufferSize;
} KERNEL_READ_KERNEL_OPERATION, *PKERNEL_READ_KERNEL_OPERATION;

typedef struct _KERNEL_COPY_MEMORY_OPERATION
{
	INT32 targetProcessId;
	PVOID targetAddress;
	PVOID bufferAddress;
	INT32 bufferSize;
} KERNEL_COPY_MEMORY_OPERATION, *PKERNEL_COPY_MEMORY_OPERATION;